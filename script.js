function dipAction(type) {
  const actions = {
    trade: () => {
      if (G.money < 100) { notify('Нет денег!', 'bad'); return; }
      G.money -= 100;
      const countries = Object.keys(G.countryRelations);
      if (countries.length > 0) {
        const rand = countries[Math.floor(Math.random() * countries.length)];
        G.countryRelations[rand] = clamp(G.countryRelations[rand] + 10, 0, 100);
      }
      addDipEvent(`[${G.year}] Торговое соглашение заключено.`);
      addHistory(G.year, 'Торговое соглашение подписано.');
      renderCountryRels(); updateHeader(); advanceYear(1);
    },
    alliance: () => {
      if (G.money < 200) { notify('Нет денег!', 'bad'); return; }
      G.money -= 200;
      addDipEvent(`[${G.year}] Предложен военный союз.`);
      addHistory(G.year, 'Предложение о военном союзе отправлено.');
      advanceYear(1); updateHeader();
    },
    ultimatum: () => {
      const countries = Object.keys(G.countryRelations);
      if (countries.length > 0) {
        const rand = countries[Math.floor(Math.random() * countries.length)];
        G.countryRelations[rand] = clamp(G.countryRelations[rand] - 25, 0, 100);
        addDipEvent(`[${G.year}] Ультиматум предъявлен. Напряжённость возросла.`);
        addHistory(G.year, 'Ультиматум предъявлен соседней державе.');
        addEvent('📜 Ультиматум ухудшил международную обстановку', 'warning');
        renderCountryRels();
      }
    },
    spy: () => {
      const roll = Math.random();
      if (roll > 0.6) {
        G.money += 150;
        addDipEvent(`[${G.year}] Разведка успешна. Получена ценная информация (+150💰).`);
        addEvent('🕵 Шпионская операция прошла успешно!', 'good');
      } else {
        const countries = Object.keys(G.countryRelations);
        if (countries.length > 0) {
          const rand = countries[Math.floor(Math.random() * countries.length)];
          G.countryRelations[rand] = clamp(G.countryRelations[rand] - 15, 0, 100);
          addDipEvent(`[${G.year}] Шпион разоблачён. Дипломатический скандал!`);
          addEvent('🕵 Шпион провалился! Международный скандал!', 'danger');
          renderCountryRels();
        }
      }
      advanceYear(1); updateHeader();
    },
    summit: () => {
      if (G.money < 300) { notify('Нет денег!', 'bad'); return; }
      G.money -= 300;
      Object.keys(G.countryRelations).forEach(k => {
        G.countryRelations[k] = clamp(G.countryRelations[k] + 5, 0, 100);
      });
      addDipEvent(`[${G.year}] Международный саммит прошёл успешно. Все отношения улучшились.`);
      addHistory(G.year, 'Международный саммит проведён под нашей эгидой.');
      addEvent('🏛 Саммит укрепил ваш авторитет!', 'good');
      renderCountryRels(); advanceYear(2); updateHeader();
    },
  };
  if (actions[type]) actions[type]();
}

