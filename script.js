function renderEras() {
  const list = document.getElementById('erasList');
  if (!list) return;
  list.innerHTML = '';
  ERAS.forEach(era => {
    const isActive = G.year >= era.years[0] && G.year <= era.years[1];
    const isCompleted = G.year > era.years[1];
    const div = document.createElement('div');
    div.className = 'era-item' + (isActive ? ' active' : '') + (isCompleted ? ' completed' : '');
    div.innerHTML = `<span class="era-years">${era.years[0]}–${era.years[1]}</span><span class="era-name">${era.name}</span>`;
    list.appendChild(div);
  });
}

