function getActiveChars() {
  return Object.values(CHARACTERS).filter(c => {
    const years = c.activeYears;
    return G.year >= years[0] - 5 && G.year <= years[1] + 5;
  });
}

function renderCharsList() {
  const list = document.getElementById('charsList');
  if (!list) return;
  list.innerHTML = '';
  const chars = Object.values(CHARACTERS);
  chars.forEach(c => {
    const rel = G.relations[c.id] ?? 50;
    const div = document.createElement('div');
    div.className = 'char-list-item' + (G.currentChar === c.id ? ' active' : '');
    div.onclick = () => selectChar(c.id);
    div.innerHTML = `
      <div class="char-list-name">${c.name}</div>
      <div class="char-list-title-small">${c.title}</div>
      <div class="char-list-rel"><div class="char-list-rel-fill" style="width:${rel}%"></div></div>`;
    list.appendChild(div);
  });
}

function selectChar(cid) {
  G.currentChar = cid;
  const char = CHARACTERS[cid];
  if (!char) return;

  const rel = G.relations[cid] ?? 50;

  // Портрет
  const portrait = document.getElementById('charPortrait');
  const placeholder = document.getElementById('charPlaceholder');
  if (char.portraitUrl) {
    portrait.src = char.portraitUrl;
    portrait.style.display = 'block';
    placeholder.style.display = 'none';
  } else {
    portrait.style.display = 'none';
    placeholder.style.display = 'flex';
  }

  document.getElementById('charName').textContent = char.name;
  document.getElementById('charTitle').textContent = char.title;
  document.getElementById('charRelBar').style.width = rel + '%';
  document.getElementById('charRelVal').textContent = rel;

  const mood = rel >= 80 ? '❤ Тёплые' : rel >= 60 ? '😊 Хорошие' : rel >= 40 ? '😐 Нейтральные' : rel >= 20 ? '😤 Холодные' : '😠 Враждебные';
  document.getElementById('charMood').textContent = mood;

  // Получаем текущую стадию
  const stageKey = G.dialogStage[cid] || 'intro';
  showDialogStage(cid, stageKey);

  renderCharsList();
}

function showDialogStage(cid, stageKey) {
  const char = CHARACTERS[cid];
  if (!char || !char.stages[stageKey]) {
    // Fallback
    showDialogStage(cid, 'intro');
    return;
  }
  const stage = char.stages[stageKey];
  G.dialogStage[cid] = stageKey;

  document.getElementById('dialogSpeaker').textContent = stage.speaker || char.name;

  const text = typeof stage.text === 'function' ? stage.text() : stage.text;
  typeText('dialogText', text);

  renderChoices(cid, stage.choices || []);
}

function typeText(elId, text) {
  const el = document.getElementById(elId);
  if (!el) return;
  el.textContent = '';
  el.style.animation = 'none';
  void el.offsetWidth;
  el.style.animation = '';
  let i = 0;
  const interval = setInterval(() => {
    el.textContent += text[i];
    i++;
    if (i >= text.length) clearInterval(interval);
  }, 18);
}

function renderChoices(cid, choices) {
  const box = document.getElementById('dialogChoices');
  if (!box) return;
  box.innerHTML = '';
  choices.forEach(ch => {
    const btn = document.createElement('button');
    btn.className = 'choice-btn ' + (ch.type || 'neutral');
    btn.innerHTML = `${ch.text}<span class="choice-effect">${ch.effect || ''}</span>`;
    btn.onclick = () => chooseOption(cid, ch);
    box.appendChild(btn);
  });
  // Кнопка "продолжить год"
  const advBtn = document.createElement('button');
  advBtn.className = 'choice-btn neutral';
  advBtn.innerHTML = '⏩ Следующий год (+1 год)<span class="choice-effect">Продвинуться во времени</span>';
  advBtn.onclick = () => { advanceYear(1); renderEras(); notify('Год ' + G.year, ''); };
  box.appendChild(advBtn);
}

function chooseOption(cid, choice) {
  G.relations[cid] = clamp((G.relations[cid] ?? 50) + (choice.rel || 0), 0, 100);

  const rel = G.relations[cid];
  document.getElementById('charRelBar').style.width = rel + '%';
  document.getElementById('charRelVal').textContent = rel;

  const mood = rel >= 80 ? '❤ Тёплые' : rel >= 60 ? '😊 Хорошие' : rel >= 40 ? '😐 Нейтральные' : rel >= 20 ? '😤 Холодные' : '😠 Враждебные';
  document.getElementById('charMood').textContent = mood;

  if (choice.rel > 0) addEvent(`💬 Разговор с ${CHARACTERS[cid].name}: отношения улучшились`, 'good');
  if (choice.rel < 0) addEvent(`💬 Разговор с ${CHARACTERS[cid].name}: отношения ухудшились`, 'warning');

  addHistory(G.year, `Беседа с ${CHARACTERS[cid].name}. Отношения: ${rel}.`);
  renderCharsList();

  // Переход к следующей стадии
  if (choice.next) {
    setTimeout(() => showDialogStage(cid, choice.next), 400);
  }
}

