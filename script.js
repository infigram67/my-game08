function getLeader() {
  const leaders = ERA_LEADERS[G.country] || [];
  for (let l of leaders) {
    if (G.year >= l.years[0] && G.year <= l.years[1]) return l.leader;
  }
  return 'Глава государства';
}

function getYear() { return G.year; }

function getCountryName() {
  return { germany: '🇩🇪 ГЕРМАНИЯ', ussr: '🇷🇺 СССР/РОССИЯ', usa: '🇺🇸 США' }[G.country] || '';
}

function updateHeader() {
  document.getElementById('headerCountry').textContent = getCountryName();
  document.getElementById('headerDate').textContent = G.year + ' год';
  document.getElementById('headerLeader').textContent = getLeader();
  document.getElementById('hMoney').textContent = '💰 ' + G.money;
  document.getElementById('hArmy').textContent = '⚔ ' + G.army;
  document.getElementById('hEconomy').textContent = '🏭 ' + G.economy;
  document.getElementById('hPeople').textContent = '👥 ' + G.welfare;
}

function updateInternalUI() {
  const bars = ['army', 'econ', 'people', 'prop', 'sci'];
  const vals = [G.army, G.economy, G.welfare, G.propaganda, G.science];
  bars.forEach((b, i) => {
    const bar = document.getElementById(b + 'Bar');
    const stat = document.getElementById(b === 'econ' ? 'econStat' : b === 'people' ? 'peopleStat' : b === 'prop' ? 'propStat' : b === 'sci' ? 'sciStat' : 'armyStat');
    if (bar) bar.style.width = vals[i] + '%';
    if (stat) stat.textContent = vals[i] + '/100';
  });
  updateHeader();
}

function notify(msg, type = '') {
  const el = document.getElementById('notification');
  el.textContent = msg;
  el.className = 'notification ' + type;
  setTimeout(() => { el.classList.add('hidden'); }, 3000);
}

function addHistory(year, text) {
  G.history.push({ year, text });
  const log = document.getElementById('historyLog');
  if (log) {
    const entry = document.createElement('div');
    entry.className = 'history-entry';
    entry.innerHTML = `<span class="history-year">${year}</span><span class="history-text">${text}</span>`;
    log.insertBefore(entry, log.firstChild);
  }
}

function addEvent(text, type = '') {
  G.events.push(text);
  const list = document.getElementById('eventsList');
  if (list) {
    const item = document.createElement('div');
    item.className = 'event-item ' + type;
    item.textContent = text;
    list.insertBefore(item, list.firstChild);
    if (list.children.length > 20) list.removeChild(list.lastChild);
  }
}

